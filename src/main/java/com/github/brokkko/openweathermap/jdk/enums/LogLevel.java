package com.github.brokkko.openweathermap.jdk.enums;

/**
 * Represents log levels.
 */
public enum LogLevel {
    /** Debug-level logging, used for detailed internal information. */
    DEBUG,

    /** Informational messages about normal operations. */
    INFO,

    /** Warnings about non-critical but unexpected conditions. */
    WARN,

    /** Errors indicating failures or unexpected exceptions. */
    ERROR,

    /** Disables all logging output. */
    OFF
}
